package tictac.test;

import java.io.Serializable;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.Table;

/**
 *
 * @author bkt
 */
@Entity
@Table(name = "tests")
public class Test implements Serializable {
    @Id
    @GeneratedValue
    @Column(name = "id")
    protected long _id;
    
    @Column(name = "name")
    protected String _name;
    
    @Column(name = "categoryid")
    protected long _categoryid;
    
    @Column(name = "qtime")
    protected int _time;
    
    @Column(name = "numq")
    protected int _numq;
    
    @Column(name = "difficulty")
    protected int _difficulty;
    
    @Column(name = "authorid")
    protected long _authorid;
    
    @Column(name = "useallq")
    protected int _useAllQuestions;
    
    
    
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public Test() {
        this._difficulty = 0;
        this._name = "";
        this._numq = 0;
        this._time = 0;
        this._categoryid = 0;
        this._authorid = 0;
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public long getAuthorid() {
        return _authorid;
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public void setAuthorid(int _authorid) {
        this._authorid = _authorid;
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public long getCategoryid() {
        return _categoryid;
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public void setCategoryid(int _categoryid) {
        this._categoryid = _categoryid;
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public int getDifficulty() {
        return _difficulty;
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public void setDifficulty(int _difficulty) {
        this._difficulty = _difficulty;
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public String getName() {
        return _name;
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public void setName(String _name) {
        this._name = _name;
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public int getNumq() {
        return _numq;
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public void setNumq(int _numq) {
        this._numq = _numq;
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public long getId() {
        return _id;
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public void setId(int _id) {
        this._id = _id;
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public int getTime() {
        return _time;
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public void setTime(int _time) {
        this._time = _time;
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public int getUseAllQuestions() {
        return _useAllQuestions;
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public void setUseAllQuestions(int _useAllQuestions) {
        this._useAllQuestions = _useAllQuestions;
    }
    
    
}
